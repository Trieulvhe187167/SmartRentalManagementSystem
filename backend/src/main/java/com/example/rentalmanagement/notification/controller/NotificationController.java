@RestController
@RequestMapping("/api/v1")
public class NotificationController {
    private final NotificationService notifications;

    public NotificationController(NotificationService notifications) {
        this.notifications = notifications;
    }

    @GetMapping("/notifications")
    public ApiResponse<PageResponse<Notification>> mine(Pageable pageable) {
        return ApiResponse.success(PageResponse.from(notifications.mine(pageable)));
    }

    @GetMapping("/notifications/unread-count")
    public ApiResponse<Long> unread() {
        return ApiResponse.success(notifications.unreadCount());
    }

    @PutMapping("/notifications/{id}/read")
    public ApiResponse<Notification> read(@PathVariable Long id) {
        return ApiResponse.success(notifications.markRead(id));
    }

    @PutMapping("/notifications/read-all")
    public ApiResponse<Void> readAll() {
        notifications.markAllRead();
        return ApiResponse.success(null);
    }

    @PostMapping("/admin/notifications/general")
    @ResponseStatus(HttpStatus.CREATED)
    public ApiResponse<Notification> general(@Valid @RequestBody NotificationGeneralRequest request) {
        return ApiResponse.success("Created", notifications.createGeneral(request));
    }
}
